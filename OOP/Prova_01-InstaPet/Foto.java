
public class Foto {

    private String url;
    private String descricao;
    
    // Getters
    public String getUrl ()                             { return this.url; }
    public String getDescricao ()                       { return this.descricao; }

    // Setters
    public void setUrl (String url_p)                   { this.url = url_p; }
    public void setDescricao (String descricao_p)       { this.descricao = descricao_p; }

}